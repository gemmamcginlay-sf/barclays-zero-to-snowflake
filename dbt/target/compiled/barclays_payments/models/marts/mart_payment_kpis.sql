WITH payments AS (
    SELECT * FROM BARCLAYS_DEMO.ANALYTICS.stg_payments
)

SELECT
    PAYMENT_DAY AS PAYMENT_DATE,
    PAYMENT_TYPE,
    REGION,
    COUNT(*) AS TOTAL_PAYMENTS,
    SUM(AMOUNT) AS TOTAL_VOLUME,
    ROUND(100.0 * COUNT(CASE WHEN STATUS = 'COMPLETED' THEN 1 END) / COUNT(*), 2) AS SUCCESS_RATE_PCT,
    ROUND(AVG(PROCESSING_TIME_MS), 0) AS AVG_PROCESSING_TIME_MS,
    PERCENTILE_CONT(0.95) WITHIN GROUP (ORDER BY PROCESSING_TIME_MS) AS P95_PROCESSING_TIME_MS
FROM payments
GROUP BY PAYMENT_DAY, PAYMENT_TYPE, REGION