WITH source AS (
    SELECT * FROM BARCLAYS_DEMO.RAW.payments
)

SELECT
    PAYMENT_ID,
    PAYMENT_DATE,
    PAYMENT_DATE::DATE AS PAYMENT_DAY,
    ORIGINATOR_NAME,
    BENEFICIARY_NAME,
    PAYMENT_TYPE,
    CHANNEL,
    CURRENCY,
    AMOUNT,
    CASE
        WHEN AMOUNT < 5000 THEN 'RETAIL'
        WHEN AMOUNT < 100000 THEN 'CORPORATE'
        ELSE 'LARGE VALUE'
    END AS VALUE_BAND,
    STATUS,
    PROCESSING_TIME_MS,
    ERROR_CODE,
    REGION
FROM source
WHERE PAYMENT_ID IS NOT NULL